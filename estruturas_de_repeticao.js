// loop while (enquanto)

let contador = 0
while(contador < 10){
console.log("valor do contador: " + contador)
contador ++
}

// do while 
do{
    console.log("valor do contador: " + contador)
}while(contador < 10)

// for (para)

for(let i = 0; i < 10; i++){
    console.log("valor do contador: " + i)
}